package main

import "fmt"

func main() {
	// The capacity must be one.
	mutex := make(chan struct{}, 1)
	counter := 0
	increase := func() {
		mutex <- struct{}{} // lock by send
		counter++
		<-mutex // unlock by receive
	}

	increase1000 := func(done chan<- struct{}) {
		for i := 0; i < 1000; i++ {
			increase()
		}
		done <- struct{}{}
	}

	done := make(chan struct{})
	go increase1000(done)
	go increase1000(done)
	<-done
	<-done
	fmt.Println(counter) // 2000
}
